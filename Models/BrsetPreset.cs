    }

    public class DownloadLimitSection
    {
        [JsonProperty("限制数量")]
        public EnabledContentSection LimitCount { get; set; } = new();
    }

    public class CoverTagSection
    {
        [JsonProperty("总开关")]
        public EnabledOnlySection MasterSwitch { get; set; } = new();

        [JsonProperty("封面ACG预设")]
        public EnabledOnlySection ACGPreset { get; set; } = new();

        [JsonProperty("封面Token等待")]
        public EnabledOnlySection TokenWait { get; set; } = new();

        [JsonProperty("封面包含")]
        public EnabledContentSection Include { get; set; } = new();

        [JsonProperty("封面包含与")]
        public EnabledOnlySection IncludeAnd { get; set; } = new();

        [JsonProperty("封面包含或")]
        public EnabledOnlySection IncludeOr { get; set; } = new();

        [JsonProperty("封面排除")]
        public EnabledContentSection Exclude { get; set; } = new();

        [JsonProperty("封面排除与")]
