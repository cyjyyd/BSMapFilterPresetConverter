        [JsonProperty("封面排除或")]
        public EnabledOnlySection ExcludeOr { get; set; } = new();

        [JsonProperty("封面包含置信度")]
        public ContentOnlySection IncludeConfidence { get; set; } = new();

        [JsonProperty("封面排除置信度")]
        public ContentOnlySection ExcludeConfidence { get; set; } = new();
    }

    // 辅助类
    public class MinMaxSection
    {
        [JsonProperty("启用")]
        public bool Enabled { get; set; }

        [JsonProperty("min")]
        public string Min { get; set; } = "";

        [JsonProperty("max")]
        public string Max { get; set; } = "";
    }

    public class EnabledContentSection
    {
        [JsonProperty("启用")]
        public bool Enabled { get; set; }

        [JsonProperty("内容")]
        public string Content { get; set; } = "";
    }

    public class EnabledValueSection
    {
        [JsonProperty("启用")]
        public bool Enabled { get; set; }

        [JsonProperty("自动生成")]
        public bool AutoGeneratedValue { get; set; }

        [JsonProperty("排位曲")]
        public bool RankedValue { get; set; }

        // 为了向后兼容，Value属性返回任一非默认值
        [JsonIgnore]
        public bool Value
        {
            get => AutoGeneratedValue || RankedValue;
            set { AutoGeneratedValue = value; RankedValue = value; }
        }
    }

    public class EnabledPathSection
    {
        [JsonProperty("启用")]
        public bool Enabled { get; set; }

        [JsonProperty("路径")]
        public string Path { get; set; } = "";
    }

    public class EnabledOnlySection
    {
        [JsonProperty("启用")]
        public bool Enabled { get; set; }
    }

    public class ContentOnlySection
    {
        [JsonProperty("内容")]
        public string Content { get; set; } = "";
    }

    public class TagsSection
    {
        [JsonProperty("包含")]
        public TagFilterSection Include { get; set; } = new();

        [JsonProperty("排除")]
        public TagFilterSection Exclude { get; set; } = new();
    }

    public class TagFilterSection
    {
        [JsonProperty("启用")]
        public bool Enabled { get; set; }

        [JsonProperty("内容")]
        public string Content { get; set; } = "";

        [JsonProperty("与")]
        public bool And { get; set; }

        [JsonProperty("或")]
        public bool Or { get; set; }
    }
